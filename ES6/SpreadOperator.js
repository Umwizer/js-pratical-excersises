// // ### **Spread Operator (`...`)**

// // The spread operator **expands** an array or object into individual elements.


// Spreading an Array

const arr1 = [1,2,3]
const arr2 = [...arr1,4,5]
console.log(arr2)
// Spreading an Object

const obj1 = {name:"Ruth",age:22}
const obj2 = {...obj1,country:"Rwanda"}
console.log(obj2)